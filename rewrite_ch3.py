OUTLINE = """\
## 第3章详细大纲

### 核心主题
"看见却不懂"与"听见却孤独"的互补。救赎与理解不只是两个人的事——门里住过的人、路过的老人、消失的记忆，都是这条线上的一环。

### 场景1：出发（书店→巷子）约1500字
- 傍晚，细雨。温野来接夏浮。
- 简洁交代：梁书姐去见房东了（fs_002背景压力），书店提前打烊。
- 温野带她走窄巷。夏浮走在他身后半步。
- 【夏浮的听觉微感】她听见温野踩过的石板在说话——不是泛泛的"声音涌进来"，要写出具体的声音质感：石板被踩下去时发出的低沉的"咚"，像是石头在咳嗽；石缝里的雨水被挤出来时的细微的"噗"，像气泡破裂；爬山虎的根须在墙面上牵引的极轻的"嘶嘶"声，像有人在用牙齿咬住一根线。
- 【关键细节】温野的右手偶尔悬在墙壁附近——他在"看"墙壁上的温度痕迹。夏浮注意到了这个动作，但没有问。她只是注意到他的手指悬着、没有碰上去、然后缩回去的过程。这和她自己确认声音是否真实时的动作一模一样。
- 不要写"她懂那种孤独"——只写她看见他的动作，然后她的手在口袋里攥紧了。行为传递情感。

### 场景2：到达（门前）约2000字
- 到达那扇门。具体描写门的物理细节：老木头门板、石头门框、木头门槛（凹陷、磨亮）、锈成青绿色的铜门环、褪色的春联。
- 温野停下来。他的表情变了——不是兴奋，是小心翼翼的，像在靠近一件易碎品。
- 夏浮没有急着闭眼。她在等。
- 温野先开口："门槛上有温度。不是站着的形状，是坐着的形状。"
- 【温野的视觉微感——具象化】他描述他看见的：门槛左侧有一团橘红色的温度印记，像一个人侧坐的轮廓，边缘模糊，说明是长年累月反复叠加的。右手撑地的位置有五个点状的浅黄色痕迹——指尖的温度。门槛中间偏下的地方有一排断断续续的线条——是脚悬空晃动时，脚尖偶尔蹭到木头留下的。
- 温野说这些时的语气：平静、专注，像在描述一张照片的构图。但他只描述他看见的形状和颜色——他不知道那些温度代表什么情感、什么故事。他看见了"坐着的形状"，但不知道坐着的人在等什么。

### 场景3：夏浮的感知（门槛的故事）约2500字
- 夏浮闭上眼睛。
- 【夏浮的听觉微感——具象化，与温野的视觉形成互补】
  - 门槛的声音不是"涌进来"——是从木头的纹理里渗出来的，像水从干裂的泥土缝隙里慢慢往上冒。
  - 声音的质感：木头的声音是低沉的、温暖的、带着一种干燥的摩擦感，像老人的手掌搓在一起。
  - 门槛讲述的故事：小女孩坐在门槛上等妈妈。但不要写成完整的叙事段落——写成声音的片段：脚悬空晃的声音（鞋底蹭木头的"沙沙"），一声"妈妈"（童声，从很远的地方飘来），跑过去的脚步声（急促的、轻的），一双手抱起一个孩子时衣服摩擦的声音。
  - 时间跳转：声音变了。同一个位置，但脚步变重了，晃动的幅度变小了。是同一个人，长大了。回来了。坐在同一个位置，但不再晃脚。
  - 最后一层：一声叹息。不是人的叹息——是木头的叹息。门槛被坐了最后一次，然后有人站起来。一个清脆的声音：拍打裤子。然后脚步离开了。
- 【互补的"对上了"瞬间】温野刚才说"脚悬空晃动留下的线条"——夏浮听见了脚晃动时鞋底蹭木头的声音。同一个细节，一个看见形状，一个听见声音。但两人都没说出这种对应。夏浮只是睁开眼睛时，目光落在门槛那排断续线条的位置——温野注意到了她的目光落点，但也没有说。

### 场景4：温野的坦白（克制版）约1500字
- 夏浮问："你是怎么知道这些的？"
- 温野："我能看见温度。"说得很平，像在说一件普通的事。
- 他解释：物体被触碰后留下痕迹，像照片底片。他只说了这些。
- 【关键改动】他不说"我以为没有人能懂"。他不追问夏浮。他说完之后，转身继续看那扇门。沉默。
- 他提到自己的纪录片项目："这条巷子里的老房子，过几个月都要拆。我想在它们消失之前拍下来。"
- 然后他说了一句："但照片里只有形状。没有声音。"——这句话是无意识说出来的，他自己都没意识到这句话对夏浮意味着什么。
- 夏浮的反应：不是内心独白，是身体反应。她的手指在裤缝边停住了。她摘下眼镜擦了擦镜片上的雨水——但她擦了很久，比需要的久。

### 场景5：老人（第三条线）约2000字
- 巷子口传来脚步声。一个老人出现。花白头发，蓝布衫，撑一把打了补丁的黑伞。
- 老人看见他们，脚步停了一下，然后走过来。他看着那扇门。
- "你也来看这扇门啊。"
- 老人知道这扇门的故事：门是老陈家的。小女孩叫陈招娣。小时候在这门口长大的。后来嫁到外地。最后一次看见她，是七八年前回来奔丧。她妈走了。她坐在门槛上坐了一下午。
- 老人说："我路过的时候看见的。天都黑了她还坐着。后来站起来，拍拍裤子，走了。"
- 这里的"拍裤子"呼应夏浮听见的声音——夏浮的手指微微攥紧了。但她没有说话。
- 老人问："你们是来拍照片的？" 温野点头。
- 老人："多拍几张。下个月就拆了。"
- 老人离开前停下来，说了一句："春联上以前写的是'春回大地'。招娣她妈最喜欢这几个字。"
- 老人走了。
- 【主题深化】老人记得陈招娣的故事，但他以为没人在意。温野能看见门上的温度痕迹，但不知道那些痕迹属于谁。夏浮能听见门槛的声音，但没有人能听她说。三种孤独，三种"记得却无法传递"的困境——在这一刻交汇了。不要直接写出这层意思——让读者自己感受到。

### 场景6：笔记本与照片（孤独的具象化）约2000字
- 温野蹲下来拍照。胶片相机，老式的。
- 他拍门框上的温度痕迹。快门声在雨后的空气里很清脆。
- 【温野的孤独具象化】他翻开随身带的一个小本子——不是笔记本，是一叠冲洗出来的拍立得照片，用橡皮筋捆着。每张照片旁边用铅笔写着标注："门把手，有反复握过的痕迹"、"窗台，有人趴在上面看过很多次"、"台阶第三级，磨损最重"。这些标注只有他自己能看懂。他曾经把这些照片给别人看过——别人说"构图不错"或者"有氛围感"。没有人问那些铅笔标注是什么意思。
- 夏浮看见了那些照片和标注。她没有问。她只是看了一眼，然后移开了视线。
- 【夏浮的孤独具象化】她掏出自己的笔记本。翻到最新的一页，开始记录：日期、地点、门的描述、物理细节。她写得很工整。然后她停了。
- 她不写声音。她从来不在笔记本上写那些声音的具体内容。她只记外部细节。因为有一次她把某一段声音记录读给梁书姐听，梁书说"你写得真好，像散文"。从那以后她再也没读给任何人听过。
- 温野站在旁边，等她写完。他看到了她的笔记本。他没有凑过去看。但他注意到她写了很多外部描述，然后停下来，把笔放下了。
- 两个人的"记录"方式：他用照片记不下声音，她用文字记不下声音。两个人都缺了一半。不要点明——让两本"记录"并排出现就够了。

### 场景7：结尾（沉默与克制）约2000字
- 他们准备离开。沿着巷子往回走。
- 夏浮走在温野旁边，不是身后了——距离近了半步。
- 路过那个废弃的公共水龙头时，夏浮的脚步慢了一下。水龙头在她耳朵里发出声音——金属的、冷的、尖锐的。有很多人拧过那个龙头。现在它锈死了，拧不动了，但那些拧的动作还留在上面。
- 温野也注意到了那个水龙头。他的手抬起来，悬了一下，又放下了。
- 两人并排走过那个水龙头。谁都没有说话。
- 快到巷子口的时候，温野开口了。
- "你第一次知道自己能听见那些东西的时候——"
- 他停住了。
- 夏浮的脚步停了。
- 沉默。
- 她没有回答。
- 但她没有走。
- 她低下头，看着脚下的青石板。石板上有一滩积水，映着两个人的影子和头顶灰白色的天。
- 温野也没有追问。
- 他只是转过身，继续往巷子口走。
- 夏浮跟上去。
- 她的手在口袋里，手指摸到了笔记本的封皮。
- 巷子口有光。雨后的阳光从云缝里漏出来，把巷子口照得亮亮的。
- 她走到光里。
"""


async def main():
    ch2_text = (PROJECT_DIR / "chapters" / "002_v2.md").read_text(encoding="utf-8")

    # Read story bible
    story_data = PROJECT_DIR / "story_data"
    xia_fu = (story_data / "characters" / "xia_fu.yaml").read_text(encoding="utf-8")
    wen_ye = (story_data / "characters" / "wen_ye.yaml").read_text(encoding="utf-8")
    world_rules = (story_data / "world_rules.yaml").read_text(encoding="utf-8")
    foreshadowing = (story_data / "foreshadowing.yaml").read_text(encoding="utf-8")

    bible_context = f"""## 角色设定
### 夏浮
{xia_fu}

### 温野
{wen_ye}

## 世界规则
{world_rules}

## 伏笔状态
{foreshadowing}
"""

    user_message = f"""## Story Bible上下文

{bible_context}

## 前一章结尾（风格参考+衔接，最后3000字）

{ch2_text[-3000:]}

## 第3章大纲

{OUTLINE}

请根据以上大纲，撰写第3章的完整正文。严格遵循大纲的场景结构和具体要求。注意：不要提供原稿中的任何内容——这是一次全新的写作。
"""

    llm = LLMClient()
    print("正在生成第3章（全新写作）...")
    print(f"输入长度: ~{len(user_message)} 字符\n")

    response = await llm.complete(
        system=SYSTEM,
        messages=[{"role": "user", "content": user_message}],
        model=MODEL_SONNET,
        max_tokens=16384,
        temperature=0.75,
    )

    output_path = PROJECT_DIR / "chapters" / "003_v2.md"
    output_path.write_text(response.text, encoding="utf-8")

    print(f"\n生成完成!")
    print(f"字数: {len(response.text)}")
    print(f"保存到: {output_path}")
    print(f"Token: 输入 {response.usage.get('input_tokens', 0):,}, 输出 {response.usage.get('output_tokens', 0):,}")


if __name__ == "__main__":
    asyncio.run(main())
