SYSTEM = """\
你是一位顶尖的中文网络小说写手。你需要对一篇已完成的章节进行扩写——保留现有的所有好的内容，在标注的位置插入新的段落和细节。

## 核心写作要求

1. **文风一致**: 与原文完全一致——冷静、克制、有距离感的第三人称。
2. **五感描写**: 每一段新增的感知描写必须有独特的、具象的、不重复的物理细节。
3. **沉默代替抒情**: 用行为、动作、身体反应传递情感。永远不要直接写情感陈述。
4. **无缝衔接**: 扩写的内容必须与前后文自然衔接，读起来像原文本来就有这些段落。

## 微感描写铁律

1. **绝不用"X记得Y"句式**: 物体不会"记得"——只写物理现象。
2. **绝不用拟人化解读**: 不写"在说话"、"在等"、"在叫她"。
3. **绝不读心**: 微感只读物体的物理痕迹。
4. **让读者连线**: 只呈现感官细节，不替读者总结。

## 严格禁止

- ❌ "她不知道"在全文中不得出现超过2次
- ❌ 排比式内心独白
- ❌ 直白情感陈述
- ❌ 同一个比喻在相邻500字内出现两次
- ❌ 新增内容与原文重复或矛盾
- ❌ "X记得Y"、"在说话"、"在等"

## 输出要求

- 输出扩写后的完整章节正文
- 目标总字数：10000-12000字（中文字符）
- 使用简体中文
- 段落之间用空行分隔
"""

EXPANSION_NOTES = """\
## 扩写指导

以下是需要扩充的具体位置和方向。原文中好的段落全部保留，只在指定位置插入新内容。

### 扩写点1：出租屋到书店的路上 +600字
在"到了街上稍微好一些"之后（原文第27-31行），插入更多街上的细节：
- 她走在巷子里，路过几个熟悉的地方。早餐铺子的蒸屉在冒气，蒸汽碰到冷空气时凝结成水珠落在铁皮檐上，滴答滴答。这是真实的、当下的声音。但旁边的电线杆上有什么东西在嗡——她的手指不小心碰到了电线杆的铁表面。声音涌进来。她赶紧收回手。
- 她学会了在街上走路的时候把手插进口袋。不碰任何东西。不碰墙壁、不碰栏杆、不碰路边停着的自行车的车把。只碰衣服。衣服的声音她已经习惯了，不会触发新的输入。
- 路过一家杂货店，门口堆着成箱的矿泉水。塑料包装膜在阳光下反光。新的东西。干净的，安静的，几乎没有痕迹。她多看了一眼那些矿泉水箱。新东西是安全的。

### 扩写点2：书店下午——沙发场景的层次感 +800字
在"她把手收回来"之后（原文第99行），扩充她对沙发声音层次的进一步感知，以及这种"分层"带来的新认知：
- 再深一层：更模糊的声音。不是单个人的体重或坐姿，而是更基础的东西——弹簧本身的疲劳度。弹簧在长年累月的压缩和回弹中，金属晶格在微观层面发生了变化。新弹簧的声音是清脆的，像手指弹在金属上的声音。旧弹簧的声音是闷的，带着一种"钝"感，像被磨了太久的刀刃。
- 她以前从来没有听到过这么深的层次。过载后遗症像是把她的耳朵撬开了一条缝——缝里面有更深的声音，更老的声音，更接近物体本身而非使用者的声音。
- 这让她意识到了一件事：她以前听到的只是"表面"——谁坐过、怎么坐的、坐了多久。但物体本身也有声音——不是被使用的声音，是材质本身的声音。弹簧的金属、布料的棉花、木头框架的年轮。这些声音不属于任何人。它们只属于物体自己。
- 她盯着沙发。阳光从窗户照进来，落在凹陷的位置上。灰尘在光线里飘浮。

### 扩写点3：笔记本回溯——更多具体记录 +1500字
在"一架被丢弃的老钢琴"记录之后（原文第123行），增加更多她翻笔记本的内容，这是全章的情感核心：
- 她继续翻。更早的。
- 第七本笔记本的某一页：一条巷子里的石板路。记录上写着：巷子口有一棵梧桐树，树根把石板顶起来了一块，走过的时候那块石板会翘起来，踩上去再落下的时候发出"咔嗒"一声。她在那块石板旁边蹲了半个小时，听那个声音——不是人踩上去的声音，是石板本身被顶起来之后、和地面之间形成的缝隙里，雨水流过时发出的细微的汩汩声。她写的是："石头下面有水的声音。像很远的地方有人在吞口水。"
- 那条巷子现在被改建了。石板路变成了柏油路。那块会翘起来的石板不在了。那个声音也不在了。
- 第五本笔记本：一把老藤椅。放在书店门口的，不是拾光书店——是她小时候住的小区门口的杂货店。藤椅的编织纹路在手指下面有凹凸感，每一根藤条被编进去的时候都留下了弯折的应力。夏天的时候，藤条在热胀冷缩中发出细微的吱吱声。她在那把藤椅旁边写了一页："藤条在热的时候会膨胀，膨胀的时候编织的交叉点会互相挤压，挤压的时候发出吱的一声。像老人在伸懒腰。"
- 杂货店早就关了。藤椅不知道去了哪里。
- 第三本笔记本：一个公交站的候车亭。铁皮的顶棚，雨天的时候雨滴打在上面，密集的嗒嗒声。但她听见的不只是雨——是铁皮在长年风吹日晒后，螺丝松动了，顶棚的两块铁皮之间有一道缝隙，风吹过去的时候会发出一种低沉的嗡嗡声。她写的是："候车亭在刮风的时候会发出嗡的一声。像在叹气。但其实是缝隙里的空气在振动。"
- 那个候车亭被拆了——公交线路调整，站点取消。
- 她翻完了。合上笔记本。十几本笔记本里，大概有三分之一的记录对应的物体已经不存在了。那些声音被记在纸上，但纸上的文字已经和真实的声音脱节了。文字是平的，声音是立体的。文字能描述频率、质感、节奏，但装不下声音的体积。
- 她低下头，看着手里的笔记本。封面是深蓝色的，和温野的外套一个颜色。她不知道这是巧合还是什么。

### 扩写点4：温野在书店的那一个小时 +1000字
在"他在老位置坐下来"之后（原文第171行），扩充两人各自做自己的事的那个小时的细节：
- 他翻书的声音。夏浮在整理书架的时候听得见——不是微感的声音，是真实的翻页声。他的手指捏着书页的边角，拇指和食指夹住纸张，慢慢翻过去。纸张在空气中划出一道弧线，落在另一面的时候有一声很轻的"啪"。这是她听过的所有翻书声中最安静的一种。
- 他在看什么书？她偷瞄了一眼——是那本周伯没翻完的《鹿城文艺》。他不知道那是周伯在看的。他只是随手拿了一本。但夏浮知道。那本杂志被翻到中间某一页的时候会自然翻开——是周伯反复翻到那一页，把纸张的纤维压出了一个"记忆弯折"，以至于杂志放在桌上时会自动打开在那一页。
- 温野翻到了那一页。他停了下来。他的手指碰到了纸面，停了很久。他看到了什么？也许是那一页的温度痕迹——周伯的手指在那一页上停留的时间最长，留下的温度最深。
- 夏浮转过头，继续整理书架。她把一本诗集从第二排挪到第三排，又从第三排挪回来。她不知道自己在干什么。她只是在等。
- 窗外有人骑自行车经过，铃铛叮了两声。有孩子在远处喊了一声什么，听不清。书店里的空气很静，有灰尘在斜射的光线里飘浮。
- 中年女人走的时候，门铃响了一声。铃铛的声音在书店里回荡了一两秒，然后被书架吸收了——布面的、纸面的、木头的表面，都是很好的吸音材料。书店的回声从来不长。

### 扩写点5：温野走后的余韵 +500字
在"她站了很久"之后（原文第213行），在"---"分隔线之前，增加她一个人在书店里的时刻：
- 门铃的声音在空气中消散了。门关上之后，书店里重新安静了。但安静的质地变了——一个小时前的安静是空的，现在的安静是满的。有人来过，有人坐过，有人翻过书，有人看过沙发，有人说了几句话。这些痕迹都很新，新到她不需要微感就能感觉到——空气里还有他外套上淡淡的洗衣液的味道，书架上那本《鹿城文艺》被翻回了第一页（不是周伯停下的那一页），柜台上多了几个橘子和一瓶矿泉水。
- 她走到他刚才坐的位置。椅子上有一点点温度残留——不是微感看见的那种，是真实的、物理的、屁股坐过之后留在椅面上的体温。她把手放上去，感觉到那一点点温热正在消散。
- 她站起来，走到窗边。外面的雨停了。路面上有积水，反射着天边的光。
"""


async def main():
    ch5_text = (PROJECT_DIR / "chapters" / "005.md").read_text(encoding="utf-8")

    story_data = PROJECT_DIR / "story_data"
    xia_fu = (story_data / "characters" / "xia_fu.yaml").read_text(encoding="utf-8")
    wen_ye = (story_data / "characters" / "wen_ye.yaml").read_text(encoding="utf-8")

    user_message = f"""## 角色设定（简要）
### 夏浮
{xia_fu}

### 温野
{wen_ye}

## 当前章节正文（第5章，需扩写）

{ch5_text}

## 扩写指导

{EXPANSION_NOTES}

请将以上扩写内容自然地融入原文，输出扩写后的完整章节。保留原文中所有好的段落，只在指定位置插入新内容。确保前后衔接自然、无重复。目标总字数10000-12000字。
"""

    llm = LLMClient()
    print("正在扩写第5章...")
    print(f"输入长度: ~{len(user_message)} 字符\n")

    response = await llm.complete(
        system=SYSTEM,
        messages=[{"role": "user", "content": user_message}],
        model=MODEL_SONNET,
        max_tokens=16384,
        temperature=0.7,
    )

    output_path = PROJECT_DIR / "chapters" / "005.md"
    output_path.write_text(response.text, encoding="utf-8")

    # Run style linter
    from meta_writing.style_linter import StyleLinter
    linter = StyleLinter()
    issues = linter.check(response.text)
    print(linter.format_report(issues))

    print(f"\n扩写完成!")
    print(f"字数: {len(response.text)}")
    print(f"保存到: {output_path}")
    print(f"Token: 输入 {response.usage.get('input_tokens', 0):,}, 输出 {response.usage.get('output_tokens', 0):,}")


if __name__ == "__main__":
    asyncio.run(main())
