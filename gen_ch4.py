SYSTEM = """\
你是一位顶尖的中文网络小说写手。你的任务是根据大纲和Story Bible上下文，生成高质量的章节正文。

## 核心写作要求

1. **文风**: 冷静、克制、有距离感的第三人称叙事。不煽情，不抒情，用物理细节传递情感。
2. **五感描写**: 充分运用视觉、听觉、嗅觉、触觉描写，每一段感知描写必须有独特的、具象的、不重复的物理细节。
3. **对话设计**: 对话极少，每句必须同时推进情节+展现性格。大量留白。
4. **节奏**: 长短句交替。长句营造氛围，短句制造张力，独句段强调重点。
5. **沉默代替抒情**: 用行为、动作、身体反应传递情感。永远不要直接写"她感到XX"、"她懂那种XX"。
6. **角色动机**: 每个角色的行为必须有可理解的动机。

## 严格禁止（违反任何一条就是失败）

- ❌ "她不知道"在全文中不得出现超过2次
- ❌ 排比式内心独白（"她应该XX？她应该XX？她应该XX？"）
- ❌ 直白情感陈述（"她懂那种孤独"、"原来不是我一个人"、"她感觉到XX在胸口涨起来"）
- ❌ "像是在说：XXXX"这种翻译式内心解读
- ❌ 泛泛的能力描写——必须具体到能在脑子里成像
- ❌ 重复使用同一个比喻（"像"不得在相邻500字内出现超过2次）
- ❌ 同一个词在相邻段落中重复出现
- ❌ 读心术——微感只能读物体的记忆/痕迹，绝对不能读人的想法

## 世界规则（必须严格遵守）

微感的本质：感知时间在物体上留下的痕迹。
- 夏浮：听觉型微感。听见物体被使用/触碰的痕迹化为声音。
- 温野：视觉型微感。看见物体上残留的温度痕迹。
- 微感无法关闭或控制强度。
- 微感感知的是物体的记忆，不是人的想法。绝对不能写成读心术。
- 感知过载时会造成头痛、失眠、情绪崩溃。
- 高密度痕迹区域（老建筑、旧物件密集处）会让微感者感到眩晕。

## 输出要求

- 输出纯正文，不要加章节标题或元数据
- 字数目标：10000-14000字（中文字符）
- 使用简体中文
- 段落之间用空行分隔
- 用"---"分隔大的场景转换
"""

OUTLINE = """\
## 第4章详细大纲：葬礼上的潮汐

### 核心主题
禁忌层揭示：微感无法关闭，在最不合时宜的时刻涌入。微感不是天赋，是负担。

### 前情衔接
第3章结尾：温野问出"你第一次知道自己能听见那些东西的时候——"，夏浮没有回答但也没有走。梁书姐说"再给我们三个月"。夏浮说"我想多看一会儿店"。

### 场景1：书店日常（约1500字）
- 第二天或几天后。书店的日常。
- 梁书姐在柜台后面算账。夏浮在整理书架。
- 一个老顾客——周伯——像往常一样来了。他每周都来，买一本旧杂志或者翻几页书，坐在窗边的旧沙发上。他花白的头发，弯着腰，走路慢。
- 【夏浮的微感日常】周伯坐在沙发上时，沙发弹簧颤动的声音和别人不一样——他坐得很轻，像怕压坏什么。他翻杂志的声音很慢，每一页停很久，指尖蹭纸面的声音带着一种干燥的沙沙，像秋天的叶子。
- 周伯走的时候在门口停了一下。他看了书店一眼——不是看书，是看这个空间。然后他走了。
- 梁书姐说："周伯今天走得早。"夏浮没回答。她在听门框上周伯刚刚扶过的地方——那里有他手掌的声音，轻的、薄的、像一层纸。

### 场景2：消息（约800字）
- 过了一周。周伯没有来。
- 又过了三天。还是没有来。
- 梁书姐接了一个电话。放下电话后，她站在柜台后面，沉默了很久。
- "周伯走了。"她说。"前天夜里。"
- 夏浮的手停在书架上。她手里拿着一本杂志——是周伯上次没翻完的那本。
- 梁书姐说要去参加葬礼，让夏浮看店。

### 场景3：书店独处——微感开始失控（约2000字）
- 夏浮一个人在书店。
- 她坐在周伯常坐的那张旧沙发上。
- 【微感失控的起点】沙发记得周伯。弹簧的颤动方式、布面被压出的凹陷、扶手被手掌磨亮的纹路——这些都在发出声音。但今天的声音不一样：不是平时那种低沉的、背景式的嗡嗡，而是更清晰、更尖锐、更密集。像是物体知道了什么，开始用更大的声音说话。
- 她听见沙发记得的不只是周伯——是所有坐过这张沙发的人。几十个，上百个。声音叠加在一起，像一百个人同时在说话。
- 她站起来，走到柜台后面。柜台上的收银机也在响，金属部件记得多少次找零时手指的温度。台灯在响，灯泡被换过的记忆。书架在响，每一本被抽出来又塞回去的书都在说话。
- 整个书店变成了一个声音的容器。所有物件同时在说话。
- 她的太阳穴开始跳。她摘下眼镜揉了揉眼睛。声音没有停。
- 她意识到：这不是平时能忍住的那种。这是过载。
- 她强迫自己深呼吸。把声音一层层剥开——先关掉最远的（门框、窗户），再关掉中间的（书架、柜台），最后只剩下脚下地板的低频嗡嗡。
- 但她关不掉沙发的声音。沙发记得太多了。
- 她站起来，离开沙发。走到门口。推开门。外面的空气凉凉的，街上有人在走，有车在开，有正常的、真实的声音。
- 她站在门口，听着那些正常的声音，等微感的过载慢慢退下去。

### 场景4：去葬礼（约1000字）
- 她本来应该留在书店看店。但她关了店门。
- 她去了葬礼。不是因为和周伯有多亲——她和他从来没有说过超过三句话。但他每周都来。他坐在沙发上翻杂志。她听过他翻杂志的声音。那个声音现在没有了。
- 从书店走到旧礼堂，穿过几条街。傍晚的光线很暗淡。

### 场景5：葬礼——全面过载（约3000字，本章核心）
- 旧礼堂。一栋老式建筑，墙壁是灰色的水泥，地板是水磨石，天花板很高，回声很重。
- 遗像摆在正中央。周伯笑眯眯的。
- 棺材旁边站着他的女儿，穿着白色孝服。
- 夏浮站在最后面。她不认识这里的大部分人。
- 【微感全面过载——严格遵守世界规则：只读物体，不读心】
  - 礼堂的地板记得今天有多少双脚走过。每一步都留下了痕迹。有的重，有的轻。有人在原地站了很久，脚步印叠了一层又一层。
  - 棺材的木头在发出声音。很低沉的、持续的嗡嗡。新木头的声音——它才被做出来不久，还没有太多记忆。但它已经记住了一件事：被抬进来的时候，有人的手在上面停了很久。手掌贴着木板，不动。那只手的声音很重，像石头沉到水底。
  - 遗像的相框。金属边框记得被擦过很多次——不是灰尘，是手指反复描摹过的痕迹。有人经常拿着这张照片，用手指沿着边框画圈。
  - 花圈的塑料包装在响——工厂流水线的声音，机器裁切的声音，没有人的温度。
  - 烟灰缸。旧的。放在签到台旁边。里面有三根没灭干净的烟蒂。烟灰缸记得无数根烟——不是今天的，是几十年的。这个烟灰缸在很多个葬礼上出现过。它记得每一次。
  - 签到台上的笔。一支黑色水笔。被很多只手握过。每只手的力度不同——有人写名字时手在抖，有人写得很用力，把纸都按出了凹痕。
  - 所有物体的声音同时涌进来。叠加。混合。像一面墙从四面八方压过来。
- 夏浮的太阳穴在跳。耳朵里嗡嗡响。视线开始模糊。
- 她撑着墙壁往外走。手碰到墙壁的那一刻——墙壁也在说话。这栋老礼堂经历过太多场葬礼。墙壁记得每一场。声音太浓了，像沥青一样粘稠，她的手指一碰就陷进去了。
- 她跌跌撞撞地走出去。撞到了一个人。
- "你没事吧？"有人扶住她。
- 是周伯的女儿。她的手扶住夏浮的手臂。夏浮感觉到了她手臂上的声音——衣服袖子被攥过很多次。反复攥紧、松开。攥紧、松开。是今天留下的痕迹。
- "我没事。"夏浮说。

### 场景6：礼堂外（约2000字）
- 夏浮蹲在礼堂外面的台阶上。
- 外面的空气凉凉的。太阳还没完全落山，天边有一条橙红色的光。远处有人在放鞭炮。
- 她的太阳穴还在跳。耳朵里的嗡嗡声慢慢退下去，像潮水退去。
- 她蹲在那里，两只手捂着耳朵。不是因为外面的声音太大——是在等里面的声音退干净。
- 这就是微感。无法关闭。无法选择。在葬礼上，所有物体同时说话，你只能听着。你不能选择不听。你不能把耳朵捂上——因为声音不是从耳朵进来的，是从手指、从脚底、从皮肤进来的。
- 她蹲了很久。
- 一只手递过来一张纸巾。
- "擦擦脸。"
- 温野。
- 他站在她旁边。深蓝色外套，手里提着相机包。他看起来刚到——肩膀上没有灰尘，鞋底是干净的。
- 他没有问她为什么在这里。没有问她为什么蹲在台阶上。
- 他只是递了纸巾。

### 场景7：温野的解释+周伯的照片（约2000字）
- 两个人坐在台阶上。
- 温野说：周伯上周找过他。周伯找他拍了一张照片——他家的老柜子。红木的，是他老伴的嫁妆。老伴走了五年了。柜子要跟着房子一起拆。周伯想留一张照片。
- 温野拍了照片。冲洗出来给周伯的时候，周伯看了很久，说了一句："你拍的这些东西，能让我老婆也看见就好了。"
- 温野没有回答。他不知道该怎么回答。
- 现在周伯也走了。柜子还在。照片还在。但看照片的人没有了。
- 温野从相机包里拿出一张照片——那张柜子的拍立得。红木，铜把手，柜门上有一道划痕。照片旁边用铅笔写着："红木柜，反复开合痕迹，温度残留集中在左侧门板——有人经常打开左边。"
- 夏浮接过照片。手指碰到照片的那一刻，她听见了——很远的、模糊的、隔了一层的声音。柜门开合的声音。很多次。然后是布料的声音——有人在翻柜子里的衣服。
- 她把照片还给温野。
- "左边。"她说。"经常打开的是左边。"
- 温野看着她。
- "里面放的是衣服。"她说。
- 她没有解释她是怎么知道的。温野也没有问。
- 两个人坐在台阶上，看着天边那条橙红色的光慢慢变暗。

### 场景8：结尾（约1000字）
- 天黑了。葬礼的人陆续离开。
- 温野站起来。"我送你回去。"
- 夏浮摇头。"我自己走。"
- 温野没有坚持。他把那张柜子的照片留在她手里。"给你。"
- "为什么？"
- "周伯说想让他老婆也看见。"温野说。"也许你能帮他。"
- 他说完就走了。
- 夏浮坐在台阶上，手里拿着那张照片。
- 她低头看着照片上的红木柜。铜把手。那道划痕。
- 她的手指沿着照片边缘慢慢移动。声音又来了——很轻，很远，像隔了一层玻璃。柜门的声音，衣服的声音，还有一个声音——很低，很轻，像是有人在柜子前面站了很久，呼吸声落在木头上。
- 她把照片夹进笔记本。和第3章末尾那张门环照片并排。
- 两张照片。两扇门。两个不在了的人。
- 她站起来，走进夜色里。
"""


async def main():
    ch3_text = (PROJECT_DIR / "chapters" / "003.md").read_text(encoding="utf-8")

    story_data = PROJECT_DIR / "story_data"
    xia_fu = (story_data / "characters" / "xia_fu.yaml").read_text(encoding="utf-8")
    wen_ye = (story_data / "characters" / "wen_ye.yaml").read_text(encoding="utf-8")
    world_rules = (story_data / "world_rules.yaml").read_text(encoding="utf-8")
    foreshadowing = (story_data / "foreshadowing.yaml").read_text(encoding="utf-8")

    bible_context = f"""## 角色设定
### 夏浮
{xia_fu}

### 温野
{wen_ye}

## 世界规则
{world_rules}

## 伏笔状态
{foreshadowing}
"""

    user_message = f"""## Story Bible上下文

{bible_context}

## 前一章结尾（第3章最后3000字，风格参考+衔接）

{ch3_text[-3000:]}

## 第4章大纲

{OUTLINE}

请根据以上大纲，撰写第4章的完整正文。严格遵循大纲的场景结构。特别注意：微感只能读物体的记忆，绝对不能写成读心术。葬礼场景中夏浮听见的必须全部是物体的声音（棺材、地板、相框、签到笔、墙壁等），不是人的想法。
"""

    llm = LLMClient()
    print("正在生成第4章：葬礼上的潮汐...")
    print(f"输入长度: ~{len(user_message)} 字符\n")

    response = await llm.complete(
        system=SYSTEM,
        messages=[{"role": "user", "content": user_message}],
        model=MODEL_SONNET,
        max_tokens=16384,
        temperature=0.75,
    )

    output_path = PROJECT_DIR / "chapters" / "004.md"
    output_path.write_text(response.text, encoding="utf-8")

    # Update story_core.yaml
    import yaml
    core_path = story_data / "story_core.yaml"
    core = yaml.safe_load(core_path.read_text(encoding="utf-8"))
    core["current_chapter"] = 4
    core_path.write_text(yaml.dump(core, allow_unicode=True, default_flow_style=False, sort_keys=False), encoding="utf-8")

    print(f"\n生成完成!")
    print(f"字数: {len(response.text)}")
    print(f"保存到: {output_path}")
    print(f"Token: 输入 {response.usage.get('input_tokens', 0):,}, 输出 {response.usage.get('output_tokens', 0):,}")


if __name__ == "__main__":
    asyncio.run(main())
